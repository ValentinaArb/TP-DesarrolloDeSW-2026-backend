class Paciente{
    nombre
    apellido
    dni
    fechaNacimiento
    sexo

    paciente(nombre, apellido, dni, fechaNacimiento,sexo){
        this.nombre = nombre;
        this.apellido = apellido;
        this.dni = dni;
        this.fechaNacimiento = fechaNacimiento;
        this.sexo = sexo;
    }
}